package ru.nsu.fit.g1201.races.model;

public class RaceParameters {

    private int mapIndex;
    private int speed;

    public void setMapIndex(int mapIndex) {
        this.mapIndex = mapIndex;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getMapIndex() {
        return mapIndex;
    }

    public int getSpeed() {
        return speed;
    }
}
